//
//  Function:  _OALMUX_UpdatePinMux
//
//  utility routine which performs the pinmuxing.
//
static
void
_OALMUX_UpdatePinMux(
    DEVICE_PINMUX_ENTRY_t  *pPinMuxEntries
    )
{
    // validate parameters
    if (pPinMuxEntries == NULL) return;

    // iterate through list and perform pin-muxing
    while (pPinMuxEntries->offset != -1)
        {
        OUTREG16(((DWORD)_pSyscConfigContext + pPinMuxEntries->offset), 
            pPinMuxEntries->value
            );
        ++pPinMuxEntries;
        }
}

