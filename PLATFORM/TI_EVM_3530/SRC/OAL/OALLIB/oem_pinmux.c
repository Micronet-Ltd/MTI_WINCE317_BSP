//-----------------------------------------------------------------------------
// macros

// generates static arrays with the following format
//      static DEVICE_PINMUX_ENTRY_t OMAP_DEVICE_MCBSP2_D0[] = {...}
//      static DEVICE_PINMUX_ENTRY_t OMAP_DEVICE_MCBSP2_D4[] = {...}
//
#define DEVICE_PINMUX_BEGIN(devId, state) static DEVICE_PINMUX_ENTRY_t devId##_##state[] = { 
#define DEVICE_PINMUX_ENTRY(field, val)   {(DWORD)&((OMAP_SYSC_PADCONFS_REGS*)0)->field, val},
#define DEVICE_PINMUX_END(devId, state)   { -1, 0 }};

// generates static arrays with the following format
//      static DEVICE_PINMUX_t _MuxTbl_OMAP_DEVICE_MCBSP2 = {
//                                                           &OMAP_DEVICE_MCBSP2_D?,
//                                                           &OMAP_DEVICE_MCBSP2_D?,
//                                                           &OMAP_DEVICE_MCBSP2_D?,
//                                                           &OMAP_DEVICE_MCBSP2_D?,
//                                                           &OMAP_DEVICE_MCBSP2_D?
//                                                           }
//
#define MAP_DEVICESTATE_BEGIN(devId)      static DEVICE_PINMUX_t _MuxTbl_##devId = {
#define MAP_DEVICESTATE_D0(devId, state)    devId##_##state,
#define MAP_DEVICESTATE_D1(devId, state)    devId##_##state,
#define MAP_DEVICESTATE_D2(devId, state)    devId##_##state,
#define MAP_DEVICESTATE_D3(devId, state)    devId##_##state,
#define MAP_DEVICESTATE_D4(devId, state)    devId##_##state
#define MAP_DEVICESTATE_END(devId)        };

#define DEVICESTATE_MUX_NAME(devId)       &_MuxTbl_##devId

