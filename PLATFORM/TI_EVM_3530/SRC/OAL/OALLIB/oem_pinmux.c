//------------------------------------------------------------------------------
//
//  Function:  OALMux_UpdateOnDeviceStateChange
//
//  called before and after a device state change.
//
DWORD
OALMux_UpdateOnDeviceStateChange(
    UINT devId,
    UINT oldState,
    UINT newState,
    BOOL bPreStateChange
    )
{
    DWORD rc = -1;
    DEVICE_PINMUX_t *pDeviceMuxTbl;
    
    if (_rgDeviceMux[devId] == NULL) 
	    return rc;

    rc = 0;
    // Don't do anything on pre-device state notification if we are 
    // transitioning to a higher power state (lower Dn number).
    // Don't do anything on post-device state notification if we are 
    // transitioning to a lower power state (higher Dn number).
    if ((bPreStateChange == FALSE && newState < oldState) ||
        (bPreStateChange != FALSE && newState > oldState))
        {
        // device is about to be activated and after state change or
        // device is about to be de-activated and before state change
        pDeviceMuxTbl = _rgDeviceMux[devId]; 
        if ((*pDeviceMuxTbl)[newState] != (*pDeviceMuxTbl)[oldState])
            {
            _OALMUX_UpdatePinMux((*pDeviceMuxTbl)[newState]);
            }

        // save pinmux context
        OALContextUpdateDirtyRegister(HAL_CONTEXTSAVE_PINMUX);
        
        rc = 1;
        }
 
    return rc;
}


